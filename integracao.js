var valorCalculoSubtracao = valor1 - valor2;
document.write("O primeiro valor  é " + valor1 + " o segundo valor é " + valor2 + " A subtração desses valores é igual à " + valorCalculoSubtracao,"<br>");

var valorCalculoMultiplicacao = valor1 * valor2;
document.write("O primeiro valor  é " + valor1 + " o segundo valor é " + valor2 + " A multiplicação desses valores é igual à " + valorCalculoMultiplicacao,"<br>");

var valorCalculoDivisao = valor1/valor2;
document.write("O primeiro valor é " + valor1 + " o segundo valor é " + valor2 + "  A divisão desses valores é igual à " + valorCalculoDivisao,"<br>");

var valorCalculoResto = valor1%valor2;
